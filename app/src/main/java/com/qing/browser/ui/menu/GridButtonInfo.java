package com.qing.browser.ui.menu;


public class GridButtonInfo {

	private int buttonImage;
	private String buttonTitle;
	private int buttonCommand;

	public int getButtonImage() {
		return buttonImage;
	}

	public void setButtonImage(int buttonImage) {
		this.buttonImage = buttonImage;
	}

	public String getButtonTitle() {
		return buttonTitle;
	}

	public void setButtonTitle(String buttonTitle) {
		this.buttonTitle = buttonTitle;
	}

	public int getButtonCommand() {
		return buttonCommand;
	}

	public void setButtonCommand(int buttonCommand) {
		this.buttonCommand = buttonCommand;
	}

}
